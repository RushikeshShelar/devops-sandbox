course = "Python Programming"

# Lenght
print(len(course))
# First Character
print(course[0])
# Last character ([i] i = index)
print(course[-1])

# Slicing Stirng ([i:j] j is exclusive i.e from i to j-1)
print(course[0:6])
# Slice till End
print(course[7:])
# Slice From Start
print(course[:6])
# Slice the entire or Make a Copy
print(course[:])
